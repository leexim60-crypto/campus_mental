def _seed_mental_resources(db: Session) -> None:
    for title, rtype, content, url in RESOURCE_SEED_ROWS:
        exists = db.query(MentalResource).filter(MentalResource.title == title).first()
        if exists:
            continue
        db.add(
            MentalResource(
                title=title,
                type=rtype,
                content=content,
                url=url,
                create_time=datetime.now(),
            )
        )


def _update_mental_resources(db: Session) -> None:
    """按标题更新已有资源的内容（启动时自动同步最新内容到数据库）。"""
    for title, rtype, content, url in RESOURCE_SEED_ROWS:
        row = db.query(MentalResource).filter(MentalResource.title == title).first()
        if row and (row.content != content or row.url != url or row.type != rtype):
            row.content = content
            row.url = url
            row.type = rtype
    db.commit()


